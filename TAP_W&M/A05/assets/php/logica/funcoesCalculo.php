<?php
function adicao($num1, $num2)
{
    $result = $num1 + $num2;
    return ($result);
}

function subtracao($num1, $num2)
{
    $result = $num1 - $num2;
    return ($result);
}

function multiplicacao($num1, $num2)
{
    $result = $num1 * $num2;
    return ($result);
}

function divisao($num1, $num2)
{
    $result = $num1 / $num2;
    return ($result);
}

